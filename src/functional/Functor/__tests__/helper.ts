export function add4(num: number) {
  return num + 4;
}

export function multiply3(num: number) {
  return num * 3;
}

export function divide2(num: number) {
  return num / 2;
}

export function toString(num: number) {
  return num.toString();
}

export function returnUndefined(num: number): undefined {
  return undefined;
}

export function returnItselfByAny(num: any) {
  return num;
}
